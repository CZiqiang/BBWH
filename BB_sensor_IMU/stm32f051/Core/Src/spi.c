/**
  * TBP     :check carefully and change to none-block mode in future.
  * @brief  :inv spi register rw in block mode.
  * @param  hspi pointer to a SPI_HandleTypeDef structure that contains
  *               the configuration information for SPI module.
  * @retval None
  */
HAL_StatusTypeDef inv_spi_master_rw_register(BB_INV_BOARD_SPI_DEVICE spi_sel_device, uint8_t *txbuffer, uint8_t * rxbuffer, uint16_t rlen)
{
  HAL_StatusTypeDef retval = HAL_OK;


  if(BB_SPI_CS_ENABLE(spi_sel_device) != 0) 
    return HAL_ERROR;
  
  retval = HAL_SPI_TransmitReceive(&hspi1,txbuffer,rxbuffer,rlen,SPITIMEOUT);

  if(BB_SPI_CS_DISABLE(spi_sel_device)!=0) 
    return HAL_ERROR;

  return retval;
}

